public class e02_potenzaDelDue {
    public static void main(String[] args) {

        byte mediaMax1 = 2 * 6;
        short mediaMax2 = 2 * 14;
        int mediaMax3 = 2 * 21;
        long mediaMax4 = 2 + 30;
        long mediaMax5 = 2 * 40;
        long mediaMax6 = 2 * 80;

        System.out.println("byte: " + mediaMax1);
        System.out.println("short: " + mediaMax2);
        System.out.println("int: " + mediaMax3);
        System.out.println("long: " + mediaMax4 + ", " + mediaMax5 + ", " + mediaMax6);

    }
}