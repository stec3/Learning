public class A06_if_else {
    public static void main(String[] args){

       /*
       if() { // condizione vera

        }else { // condizione falsa
        }
        */

        int x = -2;

        if( x > 0) {
            System.out.println("x è maggiore di zero");
        }else {
            System.out.println("x NON è maggiore di zero");
        }

        boolean r = false;
        if(r) {
            System.out.println("r è vera");
        }else {
            System.out.println("r è falso");
        }

    }
}