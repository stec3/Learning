import java.util.Scanner;
import java.util.Locale;

public class A13_for {
    public static void main(String[] args) {

        Scanner scan = new Scanner(System.in);
        scan.useLocale(Locale.US);

        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }    
}
